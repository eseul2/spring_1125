package com.myaws1125.myapp.domain;

public class BookmarkVo {
	
	private String bookmark_date;
	private int bookmark_id;
	private int midx;
	private int review_id;
	private String bookmarked_at;
	private String filename;
	
	
	
	
	public String getBookmark_date() {
		return bookmark_date;
	}
	public void setBookmark_date(String bookmark_date) {
		this.bookmark_date = bookmark_date;
	}
	public int getBookmark_id() {
		return bookmark_id;
	}
	public void setBookmark_id(int bookmark_id) {
		this.bookmark_id = bookmark_id;
	}
	public int getMidx() {
		return midx;
	}
	public void setMidx(int midx) {
		this.midx = midx;
	}
	public int getReview_id() {
		return review_id;
	}
	public void setReview_id(int review_id) {
		this.review_id = review_id;
	}
	public String getBookmarked_at() {
		return bookmarked_at;
	}
	public void setBookmarked_at(String bookmarked_at) {
		this.bookmarked_at = bookmarked_at;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	
	
	

}
